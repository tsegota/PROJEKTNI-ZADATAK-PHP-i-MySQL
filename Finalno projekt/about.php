		<p>Is a side project of GSMArena team members. It’s something we do in our spare time driven by our love for gadgets. The blog is meant to be personal and as such it will express personal opinions about the topics discussed.</p>
		<p>Whether it is digital cameras and DSLRs, computers, laptops, computer hardware, robots or just plain sci-fi movies, you name it, we’ve probably got somebody keeping a close eye on it. Not because we’re paid to do it, but instead because we’re driven by natural curiosity in all things high-tech (not that we would object to any sort of payment).</p>
		<p>So quite naturally, it occurred to us that perhaps you’d be interested in getting our scoop of the most interesting tech toys of the day. We hardly miss an announcement or a demo of an exciting new gadget and we thought it’s about time we start putting those down in writing, as well.</p>
		<p>Besides providing news scoops, the GSMArena blog will give us an opportunity to review other products besides cellphones or present our research on interesting high-tech topics that don’t necessarily have something to do with mobile phones.</p>
		<p>Finally, the GSMArena blog can also be a place to share some funny stuff from the mobile industry and if you’ve come to appreciate what our team does in the field of mobile phones, you’d certainly find it interesting to see us covering other stuff as well. At least we hope you would.</p>
