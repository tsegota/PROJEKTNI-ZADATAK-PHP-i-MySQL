			<p>The light is always low, the brooding editor tasked with writing this article used for a working title. He might have a tendency for being overly dramatic, but if you don't get too fixated on the 'always' bit and substitute it for the less absolute 'a lot of the times you want to take a picture', it just might work.</p>
			<p>Which is our way of prefacing this shootout that's coming up between the Pixel 3 and the Mate 20 Pro. It's going to be a strictly after-dark clash between computational photography chief Google and handheld (long exposure) night mode pioneer Huawei to try and establish who takes the better low-light shots.</p>
			<p>Huawei has a head start in this. It's been experimenting with taking multiple images to superimpose light trails on regular low-light photos for a few generations of flagships now - only to culminate in the P20 Pro's Night mode that had us all stunned this Spring. The huge-for-a-smartphone Bayer-filter-less sensor is a great premise for low-light domination, but is only half the story - the processing algorithms need to be on point to enable the capture of sharp hand-held long exposures with multi-frame stacking.</p>
			<p>Processing is where Pixels generally rule, and Google was the trailblazer in image stacking. And to that the fact that it just knows what you're trying to capture and how it's supposed to look, on account of having seen all of humanity's photos - okay, that may be hyperbole, but then it might not be (puts on tinfoil hat). That's somewhat better applicable in portrait-style shots (which aren't the subject of this discussion), but it's not irrelevant in fine-tuning what color random stuff is supposed to be in extreme darkness.</p>
			<p>We also wanted to try and check out if and how a tripod affected the performance of the two phones - knowing that they're properly supported only leaves the processors to deal with subject motion, and not handshake. That would potentially enable longer shutter speeds, even if the concept of shutter speed is moot with these pseudo-long exposures.</p>
			<p>And then, we tried to compare the rendition of the same scenes in the regular auto modes of the two phones - after all, you may not always appreciate the potentially artificial look of real life you get when rendered through exposure stacking. That is, the additional exposure stacking on top of the regular exposure stacking that's more or less always on - computational photography is all around.</p>
