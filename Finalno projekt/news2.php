			<p>Samsung has been using glass backs for its flagships since 2015’s Galaxy S6. Starting next year, the cream of the Galaxy crop may be using ceramic instead. Not the base Galaxy S10, though - it will get glass to go with that flat screen.</p>
			<p>The curved 5.8” Galaxy S10 and the top 6.44” model (sometimes reported as 6.2”) will have ceramic backs, according to the latest chatter. They will be available in Black and White, at least at first. The top of the line 5G-enabled 6.7” Galaxy S10 may also have ceramic back. More storage too, up to 512GB, but will be announced at later date.
			<p>Earlier rumors pointed to several colors for the S10. In light of this new info, it looks like only the base flat-screen model will offer Yellow and Green. The coloring isn’t a limitation of the material, the Xiaomi Mi Mix 3 added color options (though previous Mix models were indeed black or white only).</p>
			<p>Why ceramic, anyway? It’s a very hard material, much more resistant to scratches than any glass. And it has a polish and shine that glass can’t match either. Unlike metal, however, it doesn’t block signals, so there’s no need to sacrifice reception or wireless charging.</p>
