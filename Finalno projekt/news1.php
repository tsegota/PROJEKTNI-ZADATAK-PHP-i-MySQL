			<p>During a special event in Shenzhen, celebrating the fifth anniversary of ColorOS, Oppo announced the next installment of its launcher - ColorOS 6.0 - and mentioned that ColorOS is installed on more than 250 million devices in over 140 countries and regions.</p>
			<p>The new OS is based on light color scheme, mainly mixing a white backdrop with light color gradients. Oppo says the choice of white is meant to create an airy atmosphere.</p>
			<p>Oppo claims that ColorOS 6.0 is designed with bezeless phones in mind but it will also work on older Oppo devices. Speaking of devices, Oppo hasn't officially shared the list of supported phones just yet.</p>
			<p>A major part of the OS' look is a new universal front - Oppo Sans - which Oppo has developed in cooperation with known Chinese typeface firm Hanyi.</p>
			<p>ColorOS 6.0 has built-in machine learning that will freeze apps in the background, instead of closing them. The so-called AI application quick freeze will analyze your app activity for 2 weeks to learn your habits.</p>
			<p>Oppo will release ColorOS 6.0 in 2019. We're yet to learn the full changelog of the OS as well as which devices will get it first.</p>
